//! Deckbuilder data: card-pool entries + preset decks.
//!
//! Two FFI-facing shapes:
//!
//! - [`CardPoolEntry`] — one entry per card the deckbuilder can offer
//!   (filtered via [`playable_pool`]). Holds the readable fields the
//!   browser UI needs: id, name, kind, cost text, colors, symbols,
//!   subtypes, ability lines, creature stats, spell timing.
//! - [`PresetDeck`] — one entry per shipped curated deck. Today: a
//!   Blue Starter and a Red Starter, each a flat `Vec<String>` of
//!   card IDs (50 entries with repetition) — the same shape
//!   `tsot_start_game`'s `deck_a_ids` / `deck_b_ids` consume.
//!
//! The gauntlet-variant decks (Ra/Rb/Hu/Go/Uu/Pr/Gg) used to ship as
//! presets here; they live on inside `sim::variants` / `sim::fitness`
//! for the EA's fitness-evaluation gauntlet, but no longer surface in
//! the preset dropdown.

use crate::card::Card;
use crate::sim::snapshot::format_cost;

/// One card available in the deckbuilder. Shape mirrors what the JS
/// UI reads from in-game `CardView` snapshots so the renderer can be
/// shared.
#[derive(Debug, Clone, serde::Serialize)]
pub struct CardPoolEntry {
    pub id: String,
    pub name: String,
    /// `CardType` serialized as its variant name: `"Creature"` /
    /// `"Spell"` / `"Artifact"` / `"Environment"` / `"Mutation"` /
    /// `"Unspecified"`.
    pub kind: crate::card::CardType,
    /// Printed cost as a readable string: `"2H"`, `"1H+1G"`, `"0"`.
    /// Same format the in-game CardView uses, generated by
    /// [`crate::sim::snapshot::format_cost`].
    pub cost_text: String,
    pub colors: Vec<String>,
    pub symbols: Vec<String>,
    pub subtypes: Vec<String>,
    /// Creatures only. `None` for non-creature cards. Stored as
    /// `Stats.x` on the card — printed power, pre-modifiers.
    pub power: Option<f32>,
    /// Creatures only. `None` for non-creature cards. Stored as
    /// `Stats.y` on the card — printed toughness, pre-modifiers.
    pub toughness: Option<f32>,
    /// Spells only — `"Instant"` or `"Sorcery"`. `None` otherwise.
    pub timing: Option<crate::card::Timing>,
    /// Rendered ability lines (the same array `assets/play.html`
    /// shows in the card body).
    pub abilities: Vec<String>,
}

/// One shipped preset deck.
#[derive(Debug, Clone, serde::Serialize)]
pub struct PresetDeck {
    /// Stable identifier: `"starter"`, `"gauntlet-ra"`, … —
    /// used as the key in the JS preset dropdown.
    pub id: String,
    /// Display name in the preset dropdown.
    pub name: String,
    /// Flat array of card IDs with repetition; length = 50 for every
    /// shipped preset. Same shape as `tsot_start_game`'s `deck_a_ids`.
    pub cards: Vec<String>,
}

